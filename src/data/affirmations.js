/**
 * Returns a random affirmation for the given mood.
 * Avoids repeating the last shown entry when possible.
 *
 * @param {string} mood
 * @param {string} [lastText] - The previously shown text to avoid repeating
 * @returns {string | null}
 */
export function getRandomAffirmation(mood, lastText) {
  const list = affirmations[mood];
  if (!list?.length) return null;

  // Filter out the last shown text so consecutive refreshes always differ
  const pool = lastText && list.length > 1 ? list.filter((t) => t !== lastText) : list;
  return pool[Math.floor(Math.random() * pool.length)];
}

/**
 * Returns a deterministic affirmation based on the current day of the year.
 * Changes daily without requiring any network call. Used as the local fallback
 * for the daily banner when the affirmations.dev API is unavailable.
 *
 * @returns {string}
 */
export function getDailyAffirmation() {
  const all = Object.values(affirmations).flat();
  const yearStart = new Date(new Date().getFullYear(), 0, 0).getTime();
  const dayOfYear = Math.floor((Date.now() - yearStart) / 86_400_000);
  return all[dayOfYear % all.length];
}

